const mongoose = require('mongoose')
const {ObjectId} = mongoose.Schema.Types
const userSchema = mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true
    },
    password:{
        type:String,
        required:true
    },
    pic:{
        type:String,
        default:"https://res.cloudinary.com/hariinstaclone/image/upload/v1594098992/default_w1xgxw.png"
    },
    followers:[{
        type:ObjectId,
        ref:"User"
    }],
    following:[{
        type:ObjectId,
        ref:"User"
    }]
})
mongoose.model('User',userSchema)
